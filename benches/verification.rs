// ─── Wire format helpers ──────────────────────────────────────────────────────

fn client_data_json(type_: &str, challenge: &[u8], origin: &str) -> Vec<u8> {
    let b64 = URL_SAFE_NO_PAD.encode(challenge);
    format!(r#"{{"type":"{type_}","challenge":"{b64}","origin":"{origin}","crossOrigin":false}}"#)
        .into_bytes()
}

/// Build authenticator data for registration (includes attested credential data).
fn reg_auth_data(rp_id: &str, sign_count: u32, cred_id: &[u8], cose_key: &[u8]) -> Vec<u8> {
    let rp_hash = webauthn::crypto::sha256(rp_id.as_bytes());
    let mut out = rp_hash.to_vec();
    out.push(0x41); // UP + AT flags
    out.extend_from_slice(&sign_count.to_be_bytes());
    out.extend_from_slice(&[0u8; 16]); // aaguid
    out.extend_from_slice(&(cred_id.len() as u16).to_be_bytes());
    out.extend_from_slice(cred_id);
    out.extend_from_slice(cose_key);
    out
}

/// Build authenticator data for authentication (no attested credential data).
fn auth_auth_data(rp_id: &str, sign_count: u32) -> Vec<u8> {
    let rp_hash = webauthn::crypto::sha256(rp_id.as_bytes());
    let mut out = rp_hash.to_vec();
    out.push(0x01); // UP flag
    out.extend_from_slice(&sign_count.to_be_bytes());
    out
}

fn attestation_object(auth_data: &[u8]) -> Vec<u8> {
    let obj = Value::Map(vec![
        (Value::Text("fmt".into()), Value::Text("none".into())),
        (Value::Text("attStmt".into()), Value::Map(vec![])),
        (
            Value::Text("authData".into()),
            Value::Bytes(auth_data.to_vec()),
        ),
    ]);
    let mut buf = Vec::new();
    ciborium::into_writer(&obj, &mut buf).unwrap();
    buf
}

fn es256_cose_key(uncompressed: &[u8]) -> Vec<u8> {
    let cose = Value::Map(vec![
        (Value::Integer(1i64.into()), Value::Integer(2i64.into())), // kty: EC2
        (Value::Integer(3i64.into()), Value::Integer((-7i64).into())), // alg: ES256
        (Value::Integer((-1i64).into()), Value::Integer(1i64.into())), // crv: P-256
        (
            Value::Integer((-2i64).into()),
            Value::Bytes(uncompressed[1..33].to_vec()),
        ), // x
        (
            Value::Integer((-3i64).into()),
            Value::Bytes(uncompressed[33..65].to_vec()),
        ), // y
    ]);
    let mut buf = Vec::new();
    ciborium::into_writer(&cose, &mut buf).unwrap();
    buf
}

