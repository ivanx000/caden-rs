//! Criterion benchmarks for WebAuthn ceremony verification.
//!
//! Measures `verify_registration` and `verify_authentication` latency for each
//! supported signing algorithm: ES256, ES384, EdDSA, and RS256.
//!
//! Input construction (key generation, encoding, signing) happens outside the
//! timed loop. Only the library verification call is measured.

