        EcdsaKeyPair::from_pkcs8(&ECDSA_P256_SHA256_ASN1_SIGNING, pkcs8.as_ref(), &rng).unwrap();
    let pub_bytes = key_pair.public_key().as_ref().to_vec();
    let cose_key = es256_cose_key(&pub_bytes);

    // Build registration inputs once.
